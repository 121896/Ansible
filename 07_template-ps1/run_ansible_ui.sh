#!/bin/bash

# ---------------------------------------------------------------------
# 전역 설정 및 변수
# ---------------------------------------------------------------------
INVENTORY="inventory"
PLAYBOOK="playbook.yml"
TITLE="DevOps Ansible 통합 관리 도구"

# ---------------------------------------------------------------------
# 기능별 함수 정의
# ---------------------------------------------------------------------


# 1. PS1 환경 파일 배포 함수
deploy_ps1() {
    dialog --title "PS1 환경 변수 배포" \
           --yesno "각 호스트별 devops 사용자의 PS1 설정을 배포하시겠습니까?" 8 55
    
    if [ $? -eq 0 ]; then
        clear
        echo "=========================================================="
        echo " [작업 시작] devops 사용자 .bashrc 설정 배포 중..."
        echo "=========================================================="
        
        # playbook.yml 실행 시 태그(tag)를 지정하여 PS1 배포 작업만 필터링해 실행
        ansible-playbook -i "$INVENTORY" "$PLAYBOOK" --tags "ps1"
        
        echo "=========================================================="
        echo "작업이 완료되었습니다. 아무 키나 누르면 메뉴로 돌아갑니다."
        echo "=========================================================="
        read -n 1 -s
    fi
}

# 2. Vim 환경 설정 배포 함수
deploy_vim_config() {
    dialog --title "Vim 환경 설정 배포" \
           --yesno "각 호스트별 devops 사용자의 Vim 설정을 배포하시겠습니까?" 8 55
    
    if [ $? -eq 0 ]; then
        clear
        echo "=========================================================="
        echo " [작업 시작] devops 사용자 .vimrc 설정 배포 중..."
        echo "=========================================================="
        
        # playbook.yml 실행 시 vim 태그만 필터링해 실행
        ansible-playbook -i "$INVENTORY" "$PLAYBOOK" --tags "vim"
        
        echo "=========================================================="
        echo "작업이 완료되었습니다. 아무 키나 누르면 메뉴로 돌아갑니다."
        echo "=========================================================="
        read -n 1 -s
    fi
}

# ---------------------------------------------------------------------
# 메인 메뉴 루프
# ---------------------------------------------------------------------
while true; do
    NPC_OUT=$(mktemp /tmp/dialog.XXXXXX)

        dialog --clear \
           --backtitle "$TITLE" \
           --title " [ 메인 메뉴 ] " \
           --menu "실행할 Ansible 배포 작업을 선택하세요:" 15 65 5 \
           "1" "devops 사용자 PS1 변수 설정 배포" \
           "2" "devops 사용자 Vim 환경 설정 배포" \
           "Q" "종료 (Exit)" 2> "$NPC_OUT"

    if [ $? -ne 0 ]; then
        rm -f "$NPC_OUT"
        clear
        break
    fi

    CHOICE=$(cat "$NPC_OUT")
    rm -f "$NPC_OUT"

    case "$CHOICE" in
        1)
            deploy_ps1
            ;;
        2)
            deploy_vim_config
            ;;
        Q|q)
            clear
            echo "스크립트를 종료합니다."
            break
            ;;
        *)
            dialog --title "에러" --msgbox "잘못된 선택입니다." 5 30
            ;;
    esac
done

clear